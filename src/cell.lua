Flag = {
	NOTHING = 0LL, 			-- 0x00000000
	
	BLOCKED = 1LL,  		-- 0x00000001
	ROOM = 2LL,				-- 0x00000002
	CORRIDOR = 4LL,			-- 0x00000004

	PERIMETER = 16LL,		-- 0x00000010
	ENTRANCE = 32LL,		-- 0x00000020
	ROOM_ID = 65472LL,		-- 0x0000FFC0

	ARCH = 65536LL,			-- 0x00010000
	DOOR = 131072LL,		-- 0x00020000
	LOCKED = 262144LL,		-- 0x00040000
	TRAPPED = 524288LL,		-- 0x00080000
	SECRET = 1048576LL,		-- 0x00100000
	PORTC = 2097152LL,		-- 0x00200000
	STAIR_DN = 4194304LL,	-- 0x00400000
	STAIR_UP = 8388608LL,	-- 0x00800000

	LABEL = 4278190080LL,	-- 0xFF000000
}

local DOORSPACE = bit.bor(
	Flag.ARCH, 
	Flag.DOOR, 
	Flag.LOCKED, 
	Flag.TRAPPED, 
	Flag.SECRET, 
	Flag.PORTC
)

Mask = {
	OPENSPACE = bit.bor(Flag.ROOM, Flag.CORRIDOR),
	DOORSPACE = DOORSPACE,
	ESPACE = bit.bor(Flag.ENTRANCE, DOORSPACE, Flag.LABEL),
	STAIRS = bit.bor(Flag.STAIR_DN, Flag.STAIR_UP),
	BLOCK_ROOM = bit.bor(Flag.BLOCKED, Flag.ROOM),
	BLOCK_CORR = bit.bor(Flag.BLOCKED, Flag.PERIMETER, Flag.CORRIDOR),
	BLOCK_DOOR = bit.bor(Flag.BLOCKED, DOORSPACE),
	ROOM_ENTRANCE = bit.bor(Flag.ROOM, Flag.ENTRANCE),
}